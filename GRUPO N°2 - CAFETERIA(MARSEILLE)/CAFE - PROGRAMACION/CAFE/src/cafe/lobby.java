    //Gestor de Impuestos
    public void getTax(int t) {
        if (t >= 10.0 && t <= 20.0) {
            tax = total * 0.18;
        } else if (t > 20.0 && t <= 40.0) {
            tax = total * 0.18;
        } else if (t > 40.0 && t <= 60.0) {
            tax = total * 0.18;
        } else if (t > 60.0 && t <= 80.0) {
            tax = total * 0.18;
        } else if (t > 80.0 && t <= 100.0) {
            tax = total * 0.18;
        } else if (t > 100.0 && t <= 150.0) {
            tax = total * 0.18;
        } else if (t > 150.0 && t <= 200.0) {
            tax = total * 0.18;
        } else if (t > 200.0) {
            tax = total * 0.18;
        }
    }

